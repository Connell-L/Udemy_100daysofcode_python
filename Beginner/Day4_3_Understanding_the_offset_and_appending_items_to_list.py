# Lists are data structures to store data in python.
# Grouped data with some connection to each other.

fruits = ["orange", "peach", "pear", "plum", "lemon", "lime"]
print(fruits)

# The lists also have an order, starting from 0.

print(fruits[0])

fruits[2] = "apple"
print(fruits)

